package DataPackage;

/**
 *
 * @author jalan
 */
public class community {
    private String name;
    private String city;

    public community(String name, String city) {
        this.name = name;
        this.city = city;
    }

    // Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
    
}
